    async def scrape_search(
        self, context, keyword: str, city: str, limit: int = 5,
    ) -> List[Dict]:
        """BOSS直聘：API 拦截模式，多次重试以应对反爬。"""
        city_code = self.get_city_code(city) if city else ""
        url = self.build_search_url(keyword, city_code)
        max_retries = 3

        for attempt in range(1, max_retries + 1):
            logger.info("BOSS: attempt %d/%d city='%s' code='%s'", attempt, max_retries, city, city_code)
            page = await context.new_page()
            captured_data: List[dict] = []

            async def _on_response(response):
                try:
                    if response.status != 200:
                        return
                    await self.on_response(response, captured_data)
                except Exception as exc:
                    logger.debug("Response capture skipped: %s", exc)

            page.on("response", _on_response)

            try:
                await page.goto(url, wait_until="domcontentloaded", timeout=30000)
                await page.bring_to_front()

                # 等待更长时间让页面加载，多次检查
                for wait_round in range(6):
                    await page.wait_for_timeout(3000)
                    if captured_data:
                        break

                final_url = page.url
                logger.info("BOSS: final_url=%s captured=%d", final_url, len(captured_data))

                if captured_data:
                    first_city = captured_data[0].get("cityName", "unknown")
                    logger.info("BOSS: success! first job city='%s' (expected='%s')", first_city, city)
                    return self._parse_jobs(captured_data)[:limit]

                logger.warning("BOSS: attempt %d got no data, retrying...", attempt)
            except Exception as exc:
                logger.warning("BOSS: attempt %d error: %s", attempt, exc)
            finally:
                await page.close()

            # 重试前随机等待
            if attempt < max_retries:
                delay = random.uniform(5, 10)
                logger.info("BOSS: waiting %.1fs before retry", delay)
                await asyncio.sleep(delay)

        logger.warning("BOSS: all %d attempts failed for city='%s'", max_retries, city)
        return []

    def _parse_jobs(self, data) -> List[Dict]:
        results = []
        if isinstance(data, list):
            job_list = data
        elif isinstance(data, dict):
            job_list = data.get("zpData", {}).get("jobList", [])
        else:
            job_list = []
        for job in job_list:
            title = job.get("jobName", "")
            company = job.get("brandName", "")
            city = job.get("cityName", "")
            salary = job.get("salaryDesc", "")
            encrypt_job_id = job.get("encryptJobId", "")
            link = f"https://www.zhipin.com/job_detail/{encrypt_job_id}.html" if encrypt_job_id else ""
            skills = " ".join(job.get("skills", [])[:5])
            experience = job.get("jobExperience", "")
            education = job.get("jobDegree", "")

            raw = f"{title}\n{company}\n{city}\n{salary}"
            if experience:
                raw += f"\n经验：{experience}"
            if education:
                raw += f"\n学历：{education}"
            if skills:
                raw += f"\n技能：{skills}"

            results.append({
                "platform": "boss",
                "source_url": link,
                "raw_text": raw.strip(),
                "job_title": title,
                "company_name": company or "未知公司",
                "city": city,
                "salary_range": salary or None,
            })
        return results
