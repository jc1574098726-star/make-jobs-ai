    def build_search_url(self, keyword: str, city_code: str) -> str:
        return f"https://www.zhipin.com/web/geek/job?query={keyword}&city={city_code}"

    async def check_login(self, page) -> bool:
        return True  # BOSS直聘不需要登录检测

    async def on_response(self, response, captured_data: list):
        resp_url = response.url
        if "joblist" not in resp_url:
            return
        raw = await response.body()
        body = _json.loads(raw)
        logger.info("BOSS API %s: type=%s", resp_url[:80], type(body).__name__)

        if isinstance(body, dict):
            jobs = body.get("zpData", {}).get("jobList", [])
        elif isinstance(body, list):
            jobs = body
        else:
            jobs = []
        if jobs:
            captured_data.extend(jobs)
            first_city = jobs[0].get("cityName", "unknown") if jobs else "empty"
            logger.info("BOSS API captured %d jobs, first city='%s', url=%s", len(jobs), first_city, resp_url[:120])

